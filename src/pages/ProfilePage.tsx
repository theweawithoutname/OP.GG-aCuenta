// src/pages/ProfilePage.tsx

import React from 'react';
import { useParams, useNavigate } from 'react-router-dom'; // ‚¨ÖÔ∏è useNavigate es crucial
import SummonerSearch from '../components/SummonerSearch'; 
import SearchForm from '../components/SearchForm'; // ‚¨ÖÔ∏è Importamos el formulario

// (La interfaz RouteParams se mantiene aqu√≠)
interface RouteParams extends Record<string, string | undefined>{
    gameName: string | undefined; 
    tagLine: string | undefined;
}

const ProfilePage: React.FC = () => {
    const navigate = useNavigate(); // ‚¨ÖÔ∏è Hook para forzar la navegaci√≥n
    const { gameName, tagLine } = useParams<RouteParams>();

    // üîë CALLBACK DE NAVEGACI√ìN: Se ejecuta cuando el SearchForm se usa en esta p√°gina
    const handleNewSearch = (gName: string, tLine: string) => {
        // Al buscar un nuevo perfil, navegamos a la nueva URL.
        // Esto hace que ProfilePage se re-renderice con los nuevos par√°metros, disparando un nuevo fetch.
        const profilePath = `/profile/${gName}/${tLine}`;
        navigate(profilePath);
    };

    if (!gameName || !tagLine) {
        return <div className="text-red-500 mt-20">Error: Datos de perfil incompletos.</div>;
    }

    return (
        <div className="w-full max-w-4xl p-4 mt-10">
            
            {/* üõë NUEVA POSICI√ìN: El SearchForm va arriba de la p√°gina de perfil */}
            <header className="mb-8 p-4 bg-white shadow rounded-lg">
               <SearchForm onSearch={handleNewSearch} /> 
            </header>
            
            {/* üéØ Motor de L√≥gica: Recibe los datos de la URL y se dispara autom√°ticamente. */}
            <SummonerSearch initialGameName={gameName} initialTagLine={tagLine} />
        </div>
    );
};

export default ProfilePage;